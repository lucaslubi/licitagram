const BATCH_SIZE = 50 // Items per LLM call (batch classification for efficiency)

const aiTriageWorker = new Worker<AiTriageJobData>(
  'ai-triage',
  async (job) => {
    const { companyId, matchIds } = job.data

    if (!process.env.DEEPSEEK_API_KEY) {
      logger.warn('DEEPSEEK_API_KEY not set — skipping AI triage')
      return
    }

    logger.info({ companyId, matchCount: matchIds.length }, 'Starting background AI triage')

    // Fetch company profile
    const { data: company } = await supabase
      .from('companies')
      .select('*')
      .eq('id', companyId)
      .single()

    if (!company) {
      logger.error({ companyId }, 'Company not found for AI triage')
      return
    }

    // Auto-generate description if missing (one-time, saved to DB)
    const companyData = company as Record<string, unknown>
    if (!companyData.descricao_servicos || String(companyData.descricao_servicos).trim().length < 10) {
      const generated = await autoGenerateDescription(companyData)
      if (generated) {
        companyData.descricao_servicos = generated
      }
    }

    const companyContext = buildCompanyContext(companyData)

    // Build company CNAEs list for CNAE validation gate
    const companyCnaes: string[] = []
    if (companyData.cnae_principal) companyCnaes.push(String(companyData.cnae_principal))
    if (Array.isArray(companyData.cnaes_secundarios)) companyCnaes.push(...(companyData.cnaes_secundarios as string[]))

    // Process in batches of BATCH_SIZE
    let triaged = 0
    let lowScoreCount = 0
    for (let i = 0; i < matchIds.length; i += BATCH_SIZE) {
      const batch = matchIds.slice(i, i + BATCH_SIZE)

      try {
        const results = await triageBatch(batch, companyContext, companyId, companyCnaes)
        triaged += results.length

        for (const r of results) {
          if (r.score < LOW_SCORE_THRESHOLD) lowScoreCount++
        }

        logger.info(
          { companyId, batch: Math.floor(i / BATCH_SIZE) + 1, triaged, total: matchIds.length },
          'AI triage batch complete',
        )
      } catch (err) {
        logger.error({ companyId, err, batch: Math.floor(i / BATCH_SIZE) + 1 }, 'AI triage batch failed')
        // Continue with next batch — don't fail the whole job
      }

      // Small delay between batches to avoid rate limits
      if (i + BATCH_SIZE < matchIds.length) {
        await new Promise((resolve) => setTimeout(resolve, 500))
      }
    }

    logger.info(
      { companyId, triaged, lowScoreCount, total: matchIds.length },
      'Background AI triage complete',
    )

    // Mark company as ready after triage
    await supabase.from('companies').update({ matching_status: 'ready' }).eq('id', companyId).eq('matching_status', 'triaging')

    // Trigger notification check after triage (debounced — fixed jobId prevents queue flooding)
    try {
      const { pendingNotificationsQueue } = await import('../queues/pending-notifications.queue')
      await pendingNotificationsQueue.add(
        `post-triage-notify-${companyId}`,
        {},
        { jobId: `post-triage-notify-${companyId}`, delay: 30_000 },
      )
      logger.info({ companyId }, 'Triggered notification check after triage (30s delay, deduped)')
    } catch {
      // Non-critical — 5-min sweep will catch it
    }

    // Enqueue semantic matching if embedding provider is available and company has embedding
    // Ollama/BGE-M3 always available locally — always check for semantic matching
    {
      try {
        const { data: co } = await supabase
          .from('companies')
          .select('embedding')
          .eq('id', companyId)
          .single()

        if (co?.embedding) {
          const { semanticMatchingQueue } = await import('../queues/semantic-matching.queue')
          await semanticMatchingQueue.add(
            `post-triage-semantic-${companyId}`,
            { companyId },
            { jobId: `post-triage-semantic-${companyId}-${Date.now()}` },
          )
          logger.info({ companyId }, 'Enqueued semantic matching after triage')
        }
      } catch {
        // Non-critical — semantic matching will run in scheduled sweep
      }
    }
  },
  {
    connection,
    concurrency: 2, // 2 companies can be triaged in parallel (reduced from 3 for memory)
    limiter: { max: 10, duration: 60_000 }, // Max 10 jobs per minute
    stalledInterval: 300_000, // 5 min stall timeout
  },
)

aiTriageWorker.on('failed', (job, err) => {
  logger.error({ jobId: job?.id, err }, 'AI triage job failed')
})

