// ─── Company Context Builder ─────────────────────────────────────────────────

function buildCompanyContext(company: Record<string, unknown>): string {
  const parts: string[] = []

  if (company.razao_social) parts.push(`Empresa: ${company.razao_social}`)
  if (company.nome_fantasia) parts.push(`Nome fantasia: ${company.nome_fantasia}`)

  const allCnaes: string[] = []
  if (company.cnae_principal) allCnaes.push(String(company.cnae_principal))
  if (Array.isArray(company.cnaes_secundarios)) allCnaes.push(...(company.cnaes_secundarios as string[]))

  const cnaeDescriptions: string[] = []
  for (const cnae of allCnaes) {
    const group = cnae.substring(0, 2)
    if (CNAE_GROUPS[group]) {
      cnaeDescriptions.push(`${cnae}: ${CNAE_GROUPS[group]}`)
    }
  }
  if (cnaeDescriptions.length > 0) {
    parts.push(`CNAEs e atividades:\n${cnaeDescriptions.join('\n')}`)
  }

  if (company.descricao_servicos) parts.push(`Servicos: ${String(company.descricao_servicos).slice(0, 1000)}`)
  if (Array.isArray(company.palavras_chave) && (company.palavras_chave as string[]).length > 0) {
    parts.push(`Palavras-chave: ${(company.palavras_chave as string[]).join(', ')}`)
  }
  if (Array.isArray(company.capacidades) && (company.capacidades as string[]).length > 0) {
    parts.push(`Capacidades: ${(company.capacidades as string[]).join(', ')}`)
  }

  return parts.join('\n')
}

// ─── Auto-generate company description from CNAEs ───────────────────────────

async function autoGenerateDescription(company: Record<string, unknown>): Promise<string | null> {
  const allCnaes: string[] = []
  if (company.cnae_principal) allCnaes.push(String(company.cnae_principal))
  if (Array.isArray(company.cnaes_secundarios)) allCnaes.push(...(company.cnaes_secundarios as string[]))

  if (allCnaes.length === 0) return null

  const cnaeDescriptions: string[] = []
  for (const cnae of allCnaes) {
    const group = cnae.substring(0, 2)
    if (CNAE_GROUPS[group]) {
      cnaeDescriptions.push(`${cnae}: ${CNAE_GROUPS[group]}`)
    }
  }

  const prompt = `Com base nos CNAEs abaixo, gere uma descricao concisa (3-5 frases) dos servicos que esta empresa oferece. Seja especifico sobre o que a empresa FAZ, nao apenas os codigos.

Empresa: ${company.razao_social || 'N/A'}
CNAEs:
${cnaeDescriptions.join('\n')}
${Array.isArray(company.palavras_chave) && (company.palavras_chave as string[]).length > 0 ? `Palavras-chave: ${(company.palavras_chave as string[]).join(', ')}` : ''}

Retorne APENAS a descricao, sem formatacao, sem markdown.`

  try {
    // Uses LLM cascade (Groq → OpenRouter models) — falls back automatically
    // if any provider is out of credits, rate limited, or down.
    const desc = (await callLLM({
      task: 'summary',
      system: 'Voce gera descricoes concisas de empresas baseado em CNAEs.',
      prompt,
    })).trim()

    if (desc && desc.length > 20) {
      await supabase
        .from('companies')
        .update({ descricao_servicos: desc })
        .eq('id', company.id as string)

      logger.info({ companyId: company.id, descLength: desc.length }, 'Auto-generated company description')
      return desc
    }
  } catch (err) {
    logger.warn({ companyId: company.id, err }, 'Failed to auto-generate description')
  }
  return null
}

